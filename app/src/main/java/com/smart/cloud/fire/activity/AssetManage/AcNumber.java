package com.smart.cloud.fire.activity.AssetManage;

public class AcNumber {
    private int total;
    private int unFinish;
    private int finish;

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getUnFinish() {
        return unFinish;
    }

    public void setUnFinish(int unFinish) {
        this.unFinish = unFinish;
    }

    public int getFinish() {
        return finish;
    }

    public void setFinish(int finish) {
        this.finish = finish;
    }
}
